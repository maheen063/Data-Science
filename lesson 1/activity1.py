import numpy as np

array1 = np.array([10, 20, 30])
array2 = np.array([1, 2, 3])

add = np.add(array1, array2)
subtract = np.subtract(array1, array2)
multiply = np.multiply(array1, array2)
divide = np.divide(array1, array2)

print("Sum of arrays:", add)
print("Difference of arrays:", subtract)
print("Multiplication of arrays:", multiply)
print("Division of arrays:", divide)